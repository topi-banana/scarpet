//! Generates `src/nodes/generated.rs` from `scarpet.ungram` and fails if the
//! checked-in file is stale. On a mismatch the file is rewritten in place —
//! re-run the test and commit the result.

use std::collections::BTreeMap;
use std::path::PathBuf;

use ungrammar::{Grammar, Rule};

#[test]
fn generated_nodes_are_current() {
    let crate_root = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let grammar: Grammar = std::fs::read_to_string(crate_root.join("scarpet.ungram"))
        .expect("read scarpet.ungram")
        .parse()
        .expect("parse scarpet.ungram");

    let generated = rustfmt(&generate(&grammar));
    let path = crate_root.join("src/nodes/generated.rs");
    let on_disk = std::fs::read_to_string(&path).unwrap_or_default();
    if normalize(&on_disk) != normalize(&generated) {
        std::fs::write(&path, &generated).expect("write generated.rs");
        panic!(
            "{} was stale and has been regenerated — re-run the test and commit it",
            path.display()
        );
    }
}

fn normalize(s: &str) -> String {
    s.replace("\r\n", "\n").trim_end().to_string()
}

/// Pipe through `rustfmt` so the emitted file always satisfies
/// `cargo fmt --check`. rustfmt ships as a rustup component alongside the
/// toolchain that builds this test, so failing loudly beats drifting quietly.
fn rustfmt(text: &str) -> String {
    use std::io::Write as _;
    use std::process::{Command, Stdio};
    let mut child = Command::new("rustfmt")
        .args(["--edition", "2024"])
        .stdin(Stdio::piped())
        .stdout(Stdio::piped())
        .spawn()
        .expect("spawn rustfmt (rustup component add rustfmt)");
    child
        .stdin
        .take()
        .expect("rustfmt stdin")
        .write_all(text.as_bytes())
        .expect("write to rustfmt");
    let out = child.wait_with_output().expect("run rustfmt");
    assert!(out.status.success(), "rustfmt rejected the generated code");
    String::from_utf8(out.stdout).expect("rustfmt output is UTF-8")
}

